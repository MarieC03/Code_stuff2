template<typename weights>
struct Gauss_Quadrature{

private:

template<int iter, typename T, typename F_t>
struct rec_sum{
  public:
  static inline double sum(F_t &f){
    auto const x = weights::x[iter];
    auto const w = weights::w[iter];
    return rec_sum<iter-1,T,F_t>::sum(f) + f(x)*w;
  };
};

template<typename T, typename F_t>
struct rec_sum<0,T,F_t>{
  static constexpr size_t iter = 0;
  public:
  static inline double sum(F_t &f){
    auto const x = weights::x[iter];
    auto const w = weights::w[iter];
    return f(x)*w;
  };
};

template<typename T, typename F_t>
static inline double Gauss_quadrature(F_t &f){
/* 
  T sum = 0;
  for(int i=0; i < weights::order; ++i){
    sum += f(weights::x[i])*weights::w[i];
  }
  return sum;
  */

  return rec_sum<weights::order-1,T,F_t>::sum(f);

};

public:
template<typename T,typename F_t>
static inline double integrate(T const a, T const b, F_t &f){


  auto const interval = b-a;

  //Gauss quadrature only works over interval of -1:1
  //so need linear transformation!
  // x -> 0.5*((b-a)*x + (b+a)) 
  
  auto rescaled_f = [&] (T const x) { return f(0.5*(interval*x + (b+a)));}; 

  return 0.5*interval*Gauss_quadrature<T>(rescaled_f);

};
};


